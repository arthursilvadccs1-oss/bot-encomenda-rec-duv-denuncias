    "  ticketCounter: 0,",
    "  c4Daily: {},",
    "  recruitAttempts: {}",
    "};",
    "",
    "function normalizeStorage(data) {",
    "  const next = {",
    "    ...STORAGE_DEFAULTS,",
    "    ...(data && typeof data === \"object\" ? data : {})",
    "  };",
    "",
    "  if (!next.panelMessageIds || typeof next.panelMessageIds !== \"object\" || Array.isArray(next.panelMessageIds)) {",
    "    next.panelMessageIds = {};",
    "  }",
    "",
    "  if (next.panelMessageId && !next.panelMessageIds.combined) {",
