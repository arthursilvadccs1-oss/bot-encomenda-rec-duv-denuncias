    "  }",
    "",
    "  return next;",
    "}",
    "",
    "function readStorage() {",
    "  try {",
    "    return normalizeStorage(JSON.parse(fs.readFileSync(STORAGE_PATH, \"utf8\")));",
    "  } catch {",
    "    return normalizeStorage();",
    "  }",
    "}",
    "function writeStorage(data) {",
    "  fs.writeFileSync(STORAGE_PATH, JSON.stringify(normalizeStorage(data), null, 2), \"utf8\");",
